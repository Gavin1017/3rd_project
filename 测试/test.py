# 定义颜色
WHITE = (255, 255, 255)
BLACK = (0, 0, 0)
RED = (255, 0, 0)  # 玩家颜色

# 玩家初始位置
player_pos = [1, 1]
block_size = 25  # 假设每个迷宫单元格的大小为20像素

# 绘制迷宫函数
def draw_maze():
    for y, row in enumerate(maze):
        for x, cell in enumerate(row):
            rect = pygame.Rect(x*block_size, y*block_size, block_size, block_size)
            if cell == 1:  # 墙壁
                pygame.draw.rect(screen, BLACK, rect)
            else:  # 通路
                pygame.draw.rect(screen, WHITE, rect)

# 绘制玩家
def draw_player():
    rect = pygame.Rect(player_pos[0]*block_size, player_pos[1]*block_size, block_size, block_size)
    pygame.draw.rect(screen, RED, rect)

# 更新玩家位置
def move_player(direction):
    x, y = player_pos
    if direction == 'UP' and maze[y-1][x] == 0:
        player_pos[1] -= 1
    elif direction == 'DOWN' and maze[y+1][x] == 0:
        player_pos[1] += 1
    elif direction == 'LEFT' and maze[y][x-1] == 0:
        player_pos[0] -= 1
    elif direction == 'RIGHT' and maze[y][x+1] == 0:
        player_pos[0] += 1

# 主游戏循环
running = True
while running:
    print(1)
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        elif event.type == pygame.KEYDOWN:
            if event.key == pygame.K_UP:
                move_player('UP')
            elif event.key == pygame.K_DOWN:
                move_player('DOWN')
            elif event.key == pygame.K_LEFT:
                move_player('LEFT')
            elif event.key == pygame.K_RIGHT:
                move_player('RIGHT')

    # 绘制迷宫和玩家
    draw_maze()
    draw_player()

    # 更新屏幕
    pygame.display.flip()

# 退出游戏
pygame.quit()


